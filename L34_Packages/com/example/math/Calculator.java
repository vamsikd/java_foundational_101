package L34_Packages.com.example.math; // NOTE: Lesson folder prefix; normally you'd start at 'com.example.math'

/** Basic arithmetic utility (public so importable). */
public class Calculator {
    public static int add(int a, int b) {
        return a + b;
    }

    public static int sub(int a, int b) {
        return a - b;
    }

    public static int mul(int a, int b) {
        return a * b;
    }
}
